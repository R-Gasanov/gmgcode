# Casting (Basics)

# Casting essentially is the idea of specifiying what datatype a variable will be

# As each datatype is limited to the potential abilities and changes that can be preformed depending of the datatype

# Here is how you do it

x = int(1)

y = float('4.2')

z = str(3.0)

print (x)
print (type(x))
# We can now see the final product with these numbers
print (y)
print (type(y))
# Now we can prove that we changed these datatypes with the 'type' statement
print (z)
print (type(z))
